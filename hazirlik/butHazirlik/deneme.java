package hazirlik.butHazirlik;

public class deneme {
    private String c;
    private String d;
    private deneme2 d2;

    public deneme(String c, String d, deneme2 d2) {
        this.c = c;
        this.d = d;
        this.d2 = d2;
    }

    public String getC() {
        return c;
    }

    public void setC(String c) {
        this.c = c;
    }

    public String getD() {
        return d;
    }

    public void setD(String d) {
        this.d = d;
    }

    public deneme2 getD2() {
        return d2;
    }

    public void setD2(deneme2 d2) {
        this.d2 = d2;
    }
}
